package model;

public class AlocacaoDeHorasModel {
    private int idProjeto;
    private String nomeProjeto;
    private String horasFeitasProj;
    private String statusProjeto;
    private String hrTotalProj;

    public int getIdProjeto() {
        return idProjeto;
    }

    public void setIdProjeto(int idProjeto) {
        this.idProjeto = idProjeto;
    }

    public String getNomeProjeto() {
        return nomeProjeto;
    }

    public void setNomeProjeto(String nomeProjeto) {
        this.nomeProjeto = nomeProjeto;
    }

    public String getHorasFeitasProj() {
        return horasFeitasProj;
    }

    public void setHorasFeitasProj(String horasFeitasProj) {
        this.horasFeitasProj = horasFeitasProj;
    }

    public String getStatusProjeto() {
        return statusProjeto;
    }

    public void setStatusProjeto(String statusProjeto) {
        this.statusProjeto = statusProjeto;
    }

    public String getHrTotalProj() {
        return hrTotalProj;
    }

    public void setHrTotalProj(String hrTotalProj) {
        this.hrTotalProj = hrTotalProj;
    }

    
    
    
}
